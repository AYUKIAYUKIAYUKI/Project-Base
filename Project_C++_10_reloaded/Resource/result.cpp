//============================================================================
// 初期設定
//============================================================================
HRESULT CResult::Init()
{
	// 全てのサウンドを停止
	CSound::GetInstance()->Stop();

	// 背景の生成
	CBg::Create(
		{ SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.5f, 0.0f },	// 位置
		{ SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.5f, 0.0f },	// サイズ
		CTexture_Manager::TYPE::BG_001);						// テクスチャ

	// BGMをかける
	CSound::GetInstance()->Play(CSound::LABEL::ENDING);

	return S_OK;
}

//============================================================================
// 終了処理
//============================================================================
void CResult::Uninit()
{
	// BGMを止める
	CSound::GetInstance()->Stop(CSound::LABEL::ENDING);

	// 基底クラスの終了処理
	CScene::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CResult::Update()
{
	if (CManager::GetKeyboard()->GetTrigger(DIK_RETURN))
	{
		CFakeScreen::GetInstance()->SetFade(MODE::TITLE);
	}
}

