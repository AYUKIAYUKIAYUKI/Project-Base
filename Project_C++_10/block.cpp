#include "block.h"
#include "manager.h"

//============================================================================
// コンストラクタ
//============================================================================
CBlock::CBlock() : CObject2D(BACK_MIDDLE)
{

}

//============================================================================
// デストラクタ
//============================================================================
CBlock::~CBlock()
{

}

//============================================================================
// 初期設定
//============================================================================
HRESULT CBlock::Init()
{
	// 基底クラスの初期設定
	HRESULT hr = CObject2D::Init();

	return hr;
}

//============================================================================
// 終了処理
//============================================================================
void CBlock::Uninit()
{
	// 基底クラスの終了処理
	CObject2D::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CBlock::Update()
{
	// 基底クラスの更新
	CObject2D::Update();
}

//============================================================================
// 描画処理
//============================================================================
void CBlock::Draw()
{
	// 基底クラスの描画処理
	CObject2D::Draw();
}

//============================================================================
// 生成
//============================================================================
CBlock* CBlock::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	// プレイヤーを生成
	CBlock* pBlock = new CBlock;

	// 生成出来ていたら初期設定
	if (pBlock != nullptr)
	{
		pBlock->SetType(TYPE::BLOCK);	// タイプを設定

		pBlock->Init();			// 基底クラスの初期設定
		pBlock->SetPos(pos);	// 中心位置の設定
		pBlock->SetSize(size);	// サイズの設定
	}

	// テクスチャを取得
	LPDIRECT3DTEXTURE9 pTex = CManager::GetRenderer()->GetTextureInstane()->GetTexture(CTexture::TEX_TYPE::BLOCK_000);

	// テクスチャを設定
	pBlock->BindTex(pTex);

	return pBlock;
}