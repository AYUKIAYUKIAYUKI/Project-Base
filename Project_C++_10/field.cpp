//============================================================================
CField::CField() : CObject3D(static_cast<int>(LAYER::FRONT))
{

}

//============================================================================
// デストラクタ
//============================================================================
CField::~CField()
{

}

//============================================================================
// 初期設定
//============================================================================
HRESULT CField::Init()
{
	// 基底クラスの初期設定
	HRESULT hr = CObject3D::Init();

	return hr;
}

//============================================================================
// 終了処理
//============================================================================
void CField::Uninit()
{
	// 基底クラスの終了処理
	CObject3D::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CField::Update()
{
	// 基底クラスの更新
	CObject3D::Update();
}

//============================================================================
// 描画処理
//============================================================================
void CField::Draw()
{
	// 基底クラスの描画処理
	CObject3D::Draw();
}

//============================================================================
// 生成
//============================================================================
CField* CField::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	// インスタンスを生成
	CField* pField = new CField;

	if (pField == nullptr)
	{ // 生成失敗
		assert(false);
	}

	pField->SetType(TYPE::BLOCK);	// タイプを設定

	pField->Init();			// 基底クラスの初期設定
	pField->SetPos(pos);	// 中心位置の設定
	pField->SetSize(size);	// サイズの設定

	// テクスチャを設定
	pField->BindTex(CManager::GetRenderer()->GetTextureInstane()->GetTexture(CTexture::TEX_TYPE::BG_000));

	return pField;
}